#include <stdlib.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return ;
	while (*s)
	{
		(*f)(i, s);
		s++;
		i++;
	}
}
