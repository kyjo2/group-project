#include <stdlib.h>

void	*ft_bzero(void *s, size_t n)
{
	unsigned char	*temp_s;
	size_t			i;

	temp_s = s;
	i = -1;
	while (++i < n)
	{
		*temp_s = 0;
		temp_s++;
	}
	return (s);
}
